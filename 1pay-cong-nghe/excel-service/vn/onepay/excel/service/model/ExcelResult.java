package vn.onepay.excel.service.model;
import java.io.Serializable;


public class ExcelResult implements Serializable{
    byte[] data;
    String passWord;
    Boolean isProtect;
    
    public ExcelResult() {
    }

    // getter and setter
    public String getPassWord() {
        return passWord;
    }
    public byte[] getData() {
        return data;
    }
    public void setData(byte[] data) {
        this.data = data;
    }

    public void setPassWord(String passWord) {
        this.passWord = passWord;
    }
    public Boolean getIsProtect() {
        return isProtect;
    }
    public void setIsProtect(Boolean isProtect) {
        this.isProtect = isProtect;
    }
}
